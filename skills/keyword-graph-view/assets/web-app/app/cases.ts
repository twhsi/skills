一、廣大心平等觀。

二、滅度無住涅槃。

三、眾生本性寂滅。

四、菩薩心無四相。

這四點全面地指出，菩薩發心的四種正觀，四種次第，四種正見，以及四種成就。

一、廣大心平等觀

經文中所謂「諸菩薩摩訶薩」，有廣義和狹義之別。廣義，是從發心上來講，泛指一切發菩提心的善男子、善女人；狹義，則從證果上而言，專指已登上初地的見道菩薩。前者包括凡夫菩薩在內，後者則僅指聖賢菩薩。不論是廣義或狹義，如來的教導付囑本無凡、聖的差別。

摩訶薩，是菩薩或大士的通稱。「摩訶」曰大，其含義有七：

第一、具有大根機。

第二、具有大智慧。

第三、信仰大乘法。

第四、悟解大乘理。

第五、修持大乘行。

第六、經僧祇劫時。

第七、證得大乘果。

