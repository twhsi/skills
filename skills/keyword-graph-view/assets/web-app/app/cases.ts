大乘正宗分第三

佛告須菩提：「諸菩薩摩訶薩應如是降伏其心：所有一切眾生之類，若卵生、若胎生、若濕生、若化生，若有色、若無色，若有想、若無想、若非有想非無想，我皆令入無餘涅槃而滅度之。

